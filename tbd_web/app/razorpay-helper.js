const crypto = require('crypto');

/**
 * Verify Razorpay webhook signature (raw JSON body).
 * Prefer RAZORPAY_WEBHOOK_SECRET from Dashboard → Webhooks; fall back to API secret.
 */
exports.verifyWebhookSignature = function (rawBodyBuffer, signatureHeader) {
    const envSecret =
        process.env.RAZORPAY_WEBHOOK_SECRET || process.env.RAZORPAY_SECRET_KEY;
    const secret = String(envSecret || '')
        .trim()
        .replace(/^"(.*)"$/, '$1');
    const signature = String(signatureHeader || '')
        .trim()
        .toLowerCase();
    if (!secret || !signature || !rawBodyBuffer) {
        return false;
    }
    const body =
        Buffer.isBuffer(rawBodyBuffer)
            ? rawBodyBuffer
            : Buffer.from(String(rawBodyBuffer), 'utf8');
    const expected = crypto
        .createHmac('sha256', secret)
        .update(body)
        .digest('hex')
		.toLowerCase();
	console.log('expected', expected);
	console.log('signature', signature);
    try {
        if (
            expected.length === 64 &&
            signature.length === 64 &&
            /^[0-9a-f]+$/.test(expected) &&
            /^[0-9a-f]+$/.test(signature)
        ) {
            return crypto.timingSafeEqual(
                Buffer.from(expected, 'hex'),
                Buffer.from(signature, 'hex'),
            );
        }
        return crypto.timingSafeEqual(
            Buffer.from(expected, 'utf8'),
            Buffer.from(signature, 'utf8'),
        );
    } catch (e) {
        return false;
    }
};
