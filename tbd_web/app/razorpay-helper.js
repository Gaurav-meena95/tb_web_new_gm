/*
    name: getPlanPrices
    description: Returns display + payment amounts for all plan/coupon combos
                 in the requested currency. INR values are hardcoded; all others
                 are dynamically converted with a markup from live exchange rates.
    param: currencyCode (string)
*/
exports.getPlanPrices = async function (currencyCode) {
    const code = (currencyCode || 'INR').toUpperCase();
    if (code === 'INR') {
        const prices = {};
        for (const [key, inr] of Object.entries(_INR_BASE)) {
            prices[key] = { display: String(inr), amount: inr * 100 };
        }
        return { prices, resolvedCurrency: 'INR', resolvedSymbol: '₹' };
    }

    const resolved = resolveIntlCurrency(code);
    const symbol = CURRENCY_SYMBOLS[resolved] || '$';
    const rates = await fetchExchangeRates();
    const prices = {};
    for (const [key, inr] of Object.entries(_INR_BASE)) {
        prices[key] = convertInrToIntl(inr, resolved, rates);
    }
    return { prices, resolvedCurrency: resolved, resolvedSymbol: symbol };
};

